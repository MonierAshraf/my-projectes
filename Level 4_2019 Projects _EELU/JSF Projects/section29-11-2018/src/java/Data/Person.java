package Data;

/**
 *
 * @author MonierAshraf
 */
public class Person {

    int age;
    String name;
    String address;
    int level;

    public Person() {
    }

    public Person(int a, String n, String ad, int l) {
        age = a;
        name = n;
        address = ad;
        level = l;

    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

}
